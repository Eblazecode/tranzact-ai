import subprocess
import datetime
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import matplotlib.patches as mpatches
import matplotlib.ticker as ticker
import warnings

try:
    import shap
except ImportError:
    subprocess.run(['pip', 'install', 'shap', '-q'])
    import shap

from sklearn.ensemble import GradientBoostingRegressor
warnings.filterwarnings('ignore')

# ══════════════════════════════════════════════════════════════════════════
#  ← ONLY CHANGE THESE 4 LINES
# ══════════════════════════════════════════════════════════════════════════
TARGET_USER_NAME  = "divine"
PREDICT_MONTH     = "jan 2026"
PREDICT_MONTH_NUM = 1
PREDICT_YEAR      = 2026
# ══════════════════════════════════════════════════════════════════════════

df['Date'] = pd.to_datetime(df['Date'])
user_df = df[df['User'].str.contains(TARGET_USER_NAME, case=False, na=False)].copy()

if user_df.empty:
    raise ValueError(f"No user found matching '{TARGET_USER_NAME}'. Check your dataset.")

# FIX 1 — USER_FULL_NAME and USER_DISPLAY_NAME defined HERE (before any chart or print uses them)
USER_FULL_NAME    = user_df['User'].iloc[0]
USER_DISPLAY_NAME = USER_FULL_NAME.split()[0].upper()

# ── MONTHLY AGGREGATION & FEATURE ENGINEERING ─────────────────────────────
monthly = (
    user_df
    .set_index('Date')
    .resample('ME')
    .agg({
        'Debit':       'sum',
        'Credit':      'sum',
        'Balance':     'mean',
        'Category':    'nunique',
        'Description': 'count'
    })
    .fillna(0)
    .reset_index()
)

monthly.rename(columns={
    'Debit':       'Total_Spent',
    'Credit':      'Total_Received',
    'Balance':     'Avg_Balance',
    'Category':    'Unique_Categories',
    'Description': 'Transaction_Count'
}, inplace=True)

monthly['Month_Label']    = monthly['Date'].dt.strftime('%b %Y')
monthly['Month_Index']    = np.arange(len(monthly))
monthly['Month_Num']      = monthly['Date'].dt.month
monthly['Spent_Lag1']     = monthly['Total_Spent'].shift(1).fillna(0)
monthly['Spent_Lag2']     = monthly['Total_Spent'].shift(2).fillna(0)
monthly['Received_Lag1']  = monthly['Total_Received'].shift(1).fillna(0)
monthly['Spent_Rolling3'] = monthly['Total_Spent'].rolling(3, min_periods=1).mean()
monthly['Spend_Momentum'] = monthly['Total_Spent'].diff().fillna(0)

FEATURE_COLS = [
    'Month_Index', 'Month_Num', 'Total_Received', 'Avg_Balance',
    'Unique_Categories', 'Transaction_Count', 'Spent_Lag1',
    'Spent_Lag2', 'Received_Lag1', 'Spent_Rolling3', 'Spend_Momentum'
]

FEATURE_LABELS = {
    'Month_Index':        'Position in data timeline (month no.)',
    'Month_Num':          'Calendar month (Jan=1 … Dec=12)',
    'Total_Received':     'Total money received this month (₦)',
    'Avg_Balance':        'Average account balance this month (₦)',
    'Unique_Categories':  'No. of different spending categories used',
    'Transaction_Count':  'Total number of transactions made',
    'Spent_Lag1':         'Total spent last month (₦)',
    'Spent_Lag2':         'Total spent 2 months ago (₦)',
    'Received_Lag1':      'Total money received last month (₦)',
    'Spent_Rolling3':     '3-month rolling average spend (₦)',
    'Spend_Momentum':     'Month-on-month spending change (₦, + = rose, - = dropped)'
}

MONEY_FEATS = {
    'Total_Spent', 'Total_Received', 'Avg_Balance',
    'Spent_Lag1', 'Spent_Lag2', 'Received_Lag1',
    'Spent_Rolling3', 'Spend_Momentum'
}

# ── SAFETY CHECK ──────────────────────────────────────────────────────────
if len(monthly) < 2:
    raise ValueError(
        f"{USER_DISPLAY_NAME} has less than 2 months of data — cannot build lag features."
    )

if len(monthly) < 5:
    print(f"⚠ WARNING: Only {len(monthly)} months of data for {USER_DISPLAY_NAME}.")
    print(f"   Forecast may be less reliable. Ideally need 6+ months.")

# ── BACKTEST SLICE: train only on months BEFORE target month ──────────────
predict_date  = pd.Timestamp(year=PREDICT_YEAR, month=PREDICT_MONTH_NUM, day=1)
monthly_train = monthly[monthly['Date'] < predict_date].copy()
actual_row    = monthly[monthly['Date'].dt.to_period('M') == predict_date.to_period('M')]
has_actual    = len(actual_row) > 0
actual_spend  = actual_row['Total_Spent'].values[0] if has_actual else None

if len(monthly_train) < 2:
    raise ValueError("Not enough months before the target month to train.")

X_train = monthly_train[FEATURE_COLS].values
y_train = monthly_train['Total_Spent'].values

# ── BUILD FUTURE MONTH FEATURE ROW ────────────────────────────────────────
last        = monthly_train.iloc[-1]
second_last = monthly_train.iloc[-2]

future_rolling3 = monthly_train['Total_Spent'].tail(3).mean()
future_momentum = last['Total_Spent'] - second_last['Total_Spent']

same_month_last_year = monthly_train[monthly_train['Month_Num'] == PREDICT_MONTH_NUM]

# FIX 4 — use .values[-1] (most recent match) instead of .values[0] (oldest match)
est_received = same_month_last_year['Total_Received'].values[-1]    if len(same_month_last_year) > 0 else monthly_train['Total_Received'].mean()
est_balance  = same_month_last_year['Avg_Balance'].values[-1]       if len(same_month_last_year) > 0 else monthly_train['Avg_Balance'].mean()
est_cats     = same_month_last_year['Unique_Categories'].values[-1] if len(same_month_last_year) > 0 else monthly_train['Unique_Categories'].mean()
est_txns     = same_month_last_year['Transaction_Count'].values[-1] if len(same_month_last_year) > 0 else monthly_train['Transaction_Count'].mean()

X_future = np.array([[
    len(monthly_train),
    PREDICT_MONTH_NUM,
    est_received,
    est_balance,
    est_cats,
    est_txns,
    last['Total_Spent'],
    second_last['Total_Spent'],
    last['Total_Received'],
    future_rolling3,
    future_momentum
]])

future_vals = dict(zip(FEATURE_COLS, X_future[0]))

# ── MODELING ──────────────────────────────────────────────────────────────
gb = GradientBoostingRegressor(
    n_estimators=200, learning_rate=0.05, max_depth=3, random_state=42
)
gb.fit(X_train, y_train)
y_pred = max(gb.predict(X_future)[0], 0)

# ── SHAP ──────────────────────────────────────────────────────────────────
explainer  = shap.TreeExplainer(gb)
shap_vals  = explainer.shap_values(X_future)[0]
shap_all   = explainer.shap_values(X_train)
base_value = float(np.array(explainer.expected_value).flatten()[0])

shap_series = pd.Series(shap_vals, index=FEATURE_COLS)

# ══════════════════════════════════════════════════════════════════════════
#  HELPER FORMATTERS
# ══════════════════════════════════════════════════════════════════════════

def fmt_val(feat, val):
    # FIX 3 — momentum label now honest: says it's the PREVIOUS month's trend used as a signal
    if feat == 'Spend_Momentum':
        if val > 0:
            return f"Last month rose by ₦{abs(val):,.2f} vs the month before (used as {PREDICT_MONTH} signal)"
        elif val < 0:
            return f"Last month dropped by ₦{abs(val):,.2f} vs the month before (used as {PREDICT_MONTH} signal)"
        else:
            return "No change between the last two months"
    elif feat in MONEY_FEATS:
        return f"₦{val:,.2f}"
    else:
        return f"{int(val)}"

def explain_impact(val):
    direction = "pushed the forecast UP" if val > 0 else "pulled the forecast DOWN"
    return f"({direction} by ₦{abs(val):,.0f})"

def clean_label(feat):
    return FEATURE_LABELS[feat].split('(')[0].strip().lower()

# ══════════════════════════════════════════════════════════════════════════
#  UNIFIED XAI SUMMARY
# ══════════════════════════════════════════════════════════════════════════

top_up   = shap_series[shap_series > 0].nlargest(3)
top_down = shap_series[shap_series < 0].nsmallest(3)
dominant = shap_series.abs().idxmax()

dominant_dir  = shap_series[dominant]
dominant_push = abs(dominant_dir)
total_up      = shap_series[shap_series > 0].sum()
total_down    = shap_series[shap_series < 0].abs().sum()

if dominant_dir > 0:
    dominant_pct   = min((dominant_push / total_up   * 100), 100) if total_up   > 0 else 0
    pct_pool_label = "upward"
else:
    dominant_pct   = min((dominant_push / total_down * 100), 100) if total_down > 0 else 0
    pct_pool_label = "downward"

if dominant_push >= 10000:
    size_word = "massively"
elif dominant_push >= 5000:
    size_word = "strongly"
elif dominant_push >= 1000:
    size_word = "moderately"
elif dominant_push >= 500:
    size_word = "slightly"
else:
    size_word = "marginally"

# ── HEADER ────────────────────────────────────────────────────────────────
header_suffix = "(BACKTEST — ACTUAL DATA EXISTS)" if has_actual else "(FUTURE — NO ACTUAL DATA)"

sep = "=" * 75
print(sep)
print(f"   {USER_DISPLAY_NAME}'S {PREDICT_MONTH.upper()} SPENDING PREDICTION {header_suffix}")
print(sep)
print(f"   Starting Baseline  : ₦{base_value:,.2f}")
print(f"   AI Forecast        : ₦{y_pred:,.2f}")
if has_actual:
    diff      = y_pred - actual_spend
    direction = "OVER" if diff > 0 else "UNDER"
    print(f"   Actual Spend       : ₦{actual_spend:,.2f}  ← real data from logs")
    print(f"   Forecast Error     : ₦{abs(diff):,.2f} {direction}estimated")
print(f"   Last Known Month   : {last['Month_Label']}  →  ₦{last['Total_Spent']:,.2f} actual spend")
print(f"   {'─' * 65}")
print(f"\n   WHY DID THE AI PREDICT ₦{y_pred:,.0f} FOR {PREDICT_MONTH.upper()}?")
print(f"\n   NOTE: The ₦ amounts below show how much each factor PUSHED")
print(f"   the forecast — NOT the actual value of that factor.")
print(f"   The model starts at ₦{base_value:,.0f} and adjusts from there.\n")

# ── Factors UP ────────────────────────────────────────────────────────────
if len(top_up) > 0:
    print("   FACTORS THAT INCREASED THE FORECAST (pushed spending UP):")
    for feat, val in top_up.items():
        print(f"   ▲ {FEATURE_LABELS[feat]}")
        print(f"     Estimated value for {PREDICT_MONTH} : {fmt_val(feat, future_vals[feat])}")
        # FIX 2 — replaced "Avg push UP by" with accurate wording
        print(f"     Pushed forecast UP by          : ₦{abs(val):,.0f}")
else:
    print("   NO FACTORS PUSHED THE FORECAST UP this month.")

# ── Factors DOWN ──────────────────────────────────────────────────────────
if len(top_down) > 0:
    print("\n   FACTORS THAT DECREASED THE FORECAST (pulled spending DOWN):")
    for feat, val in top_down.items():
        print(f"   ▼ {FEATURE_LABELS[feat]}")
        print(f"     Estimated value for {PREDICT_MONTH} : {fmt_val(feat, future_vals[feat])}")
        # FIX 2 — replaced "Avg push DOWN by" with accurate wording
        print(f"     Pulled forecast DOWN by        : ₦{abs(val):,.0f}")
else:
    print("\n   NO FACTORS PULLED THE FORECAST DOWN this month.")

# ── Bottom line ───────────────────────────────────────────────────────────
print(f"\n   BOTTOM LINE:")
print(f"   \"{FEATURE_LABELS[dominant]}\" was the biggest driver,")
print(f"   {size_word} {explain_impact(shap_series[dominant])}.")
print(f"   It accounted for {dominant_pct:.1f}% of all {pct_pool_label} pressure on the forecast.")

print(f"\n   WHAT THIS MEANS IN PLAIN ENGLISH:")
spend_word = "more" if dominant_dir > 0 else "less"

if dominant == 'Spend_Momentum':
    if dominant_dir > 0:
        print(f"   The previous month's spending dropped sharply.")
        print(f"   The AI learned that when {USER_DISPLAY_NAME} has a big spending drop,")
        print(f"   a rebound tends to follow — so it expects {USER_DISPLAY_NAME} to spend {spend_word} in {PREDICT_MONTH}.")
    else:
        print(f"   The previous month's spending rose sharply.")
        print(f"   The AI learned that when {USER_DISPLAY_NAME} has a big spending rise,")
        print(f"   a pullback tends to follow — so it expects {USER_DISPLAY_NAME} to spend {spend_word} in {PREDICT_MONTH}.")
else:
    direction_word = "HIGH" if dominant_dir > 0 else "LOW"
    print(f"   The AI sees {clean_label(dominant)} is {direction_word}")
    print(f"   compared to past months, so it expects {USER_DISPLAY_NAME} to spend {spend_word} in {PREDICT_MONTH}.")
print(sep)


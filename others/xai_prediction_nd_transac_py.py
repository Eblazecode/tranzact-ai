def run_recommendation_engine(user_name, user_df):

    user_df    = user_df.copy()
    first_name = user_name.split()[0]

    print(f"\nProcessing {user_name}...")
    params = compute_parameters(user_df)
    recs   = build_recommendations(first_name, params)

    sep  = "=" * 70
    thin = "-" * 70

    print(sep)
    print(f"  PERSONALISED FINANCIAL ANALYSIS REPORT")
    print(f"  Client                 : {user_name}")
    print(f"  Financial Health Score : {params['health_score']}/85  |  {params['health_status']}")
    print(sep)
    print(f"  Total Income   : ₦{params['total_in']:,.2f}")
    print(f"  Total Spending : ₦{params['total_out']:,.2f}")
    print(f"  Savings Rate   : {params['savings_rate']}%")
    print(f"  Leakage        : ₦{params['leakage_spend']:,.2f} ({params['leakage_pct']}%)")
    print(f"  Top Category   : {params['top_category']} — "
          f"₦{params['top_category_amt']:,.2f} ({params['top_category_pct']}%)")
    if params['shap_result']:
        direction = "increasing" if params['shap_result']['dominant_dir'] > 0 else "decreasing"
        print(f"  AI Driver      : {params['shap_result']['dominant_label']} ({direction} spend)")
    if params['has_anomaly']:
        print(f"  Anomaly Months : {', '.join(params['anomaly_months'])}")
    else:
        print(f"  Anomalies      : None detected")
    print(thin)
    print(f"  RECOMMENDATIONS")
    print(thin)

    titles = [
        "1. SAVINGS RATE",
        "2. FINANCIAL HEALTH SCORE",
        "3. FINANCIAL LEAKAGE",
        "4. TOP SPENDING CATEGORY",
        "5. SPENDING ANOMALY",
        "6. AI-DRIVEN BEHAVIOURAL INSIGHT (SHAP)",
    ]

    for title, rec in zip(titles, recs):
        print(f"\n  {title}")
        print(f"  {'_' * 66}")
        words = rec.split()
        line  = "  "
        for word in words:
            if len(line) + len(word) + 1 > 68:
                print(line)
                line = "  " + word + " "
            else:
                line += word + " "
        if line.strip():
            print(line)

    print(f"\n{sep}\n")

